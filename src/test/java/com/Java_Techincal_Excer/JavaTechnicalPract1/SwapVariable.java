package com.Java_Techincal_Excer.JavaTechnicalPract1;

public class SwapVariable {

    public static void main(String[] args)
    {

        int a=5;
        int b=7;

        System.out.println(a+"   "+b);

        int temp=a;
        a=b;
        b=temp;


        System.out.println(a+"   "+b);

    }
}
